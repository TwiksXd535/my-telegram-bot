# === СОХРАНЕНИЕ ПОЛЬЗОВАТЕЛЯ ===
async def save_user(message: types.Message):
    user = message.from_user
    now = datetime.now()
    
    if user.id not in users_db:
        users_db[user.id] = {
            "id": user.id,
            "name": user.first_name,
            "username": user.username,
            "first_seen": now.strftime("%d.%m.%Y %H:%M"),
            "last_seen": now.strftime("%d.%m.%Y %H:%M"),
            "messages": 1
        }
        if user.id != ADMIN_ID:
            print(f"🆕 НОВЫЙ ПОЛЬЗОВАТЕЛЬ: {user.first_name} (@{user.username}) ID: {user.id}")
            print(f"⏰ Время: {now.strftime('%H:%M:%S')}")
            print("-" * 30)
    else:
        users_db[user.id]["last_seen"] = now.strftime("%d.%m.%Y %H:%M")
        users_db[user.id]["messages"] += 1

