def get_random_meme():
    """Возвращает путь к случайному мему из папки memes"""
    meme_folder = "memes"
    
    # Проверяем, существует ли папка
    if not os.path.exists(meme_folder):
        os.makedirs(meme_folder)
        print(f"📁 Создана папка {meme_folder}. Положи туда мемы!")
        return None
    
    # Получаем список всех картинок в папке
    try:
        memes = [f for f in os.listdir(meme_folder) 
                if f.lower().endswith(('.png', '.jpg', '.jpeg', '.gif', '.bmp'))]
        
        if not memes:
            print(f"📁 В папке {meme_folder} нет картинок!")
            return None
        
        # Выбираем случайный файл
        random_meme = random.choice(memes)
        return os.path.join(meme_folder, random_meme)
    except Exception as e:
        print(f"❌ Ошибка при чтении папки memes: {e}")
        return None

